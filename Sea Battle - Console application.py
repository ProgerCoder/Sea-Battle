import random
def setfield():
    field = [[".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."],
             [".",".",".",".",".",".",".",".",".","."]]
    fieldX = field
    ships = { "1":{"1":[["", ""]], "2":[["", ""]], "3":[["", ""]], "4":[["", ""]]},
             "2":{"1":[["", ""], ["", ""]], "2":[["", ""], ["", ""]], "3":[["", ""], ["", ""]]},
             "3":{"1":[["", ""], ["", ""], ["", ""]], "2":[["", ""], ["", ""], ["", ""]]},
             "4":{"1":[["", ""], ["", ""], ["", ""], ["", ""]]} }
    for s in ships:
        for i in ships[s]:
            for r in range(len(ships[s][i])):
                #print(len(ships[s][i])) #all ships
                while True:
                    x = random.randint(0,9)
                    y = random.randint(0,9)
                    if fieldX[y][x] != "X":
                        ships[s][i][r-1][0] = x
                        ships[s][i][r-1][1] = y
                        fieldX[y][x] = "X"
                        break
    for i in range(0, len(fieldX)):
        print(fieldX[i])
